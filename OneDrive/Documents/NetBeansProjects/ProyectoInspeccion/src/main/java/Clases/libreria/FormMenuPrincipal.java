    private void configurarMenuSegunCargo() {
        lblBienvenida.setText("Bienvenido: " + nombreUsuario);
        lblCargo.setText("Cargo: " + nombreCargo);
        
        // Mostrar/ocultar opciones seg√∫n el cargo
        switch (nombreCargo) {
            case "Administrador ICA":
                // El administrador ve todo
                break;
            case "Propietario":
                btnGestionarProductores.setVisible(false);
                btnGestionarTecnicos.setVisible(false);
                btnGestionarCatalogos.setVisible(false);
                break;
            case "Productor":
                btnGestionarProductores.setVisible(false);
                btnGestionarPredios.setVisible(false);
                btnGestionarTecnicos.setVisible(false);
                btnGestionarCatalogos.setVisible(false);
                break;
            case "Asistente T√©cnico":
                btnGestionarProductores.setVisible(false);
                btnGestionarPredios.setVisible(false);
                btnGestionarTecnicos.setVisible(false);
                break;
        }
    }
    
